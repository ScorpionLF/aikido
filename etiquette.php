                    зале.
                    До начала занятия рекомендуется самостоятельно произвести разминку. </p>
                <p>14. Айкидо не допускает праздности и
                    разговоров; сидеть в зале следует в положении сэйдза или агура (поза портного - скрестив ноги).
                    Занятие начинается с того, что все садятся в ряд лицом к шомэн и приветствуют его поклоном дзарэй.
