                <p>1. Додзё (зал для занятий) предназначено не для самоутверждения, а для того, чтобы развивать свои
                    ум, тело и
                    дух.</p>
                <p>2. Долг каждого занимающегося - вести себя так, чтобы в додзё царила атмосфера взаимного
                    уважения.</p>
                <p>3. Уборка додзё - необходимый элемент тренировки. За чистоту зала отвечает каждый.</p>
                <p>4. Войдя в додзё, произведите рэй (поклон) в сторону шомэн (лицевую сторону зала). Это дань уважения
                    к тому
                    месту где Вы постигаете Путь.</p>
                <p>5. Перед занятием необходимо снять на время занятий все украшения (кольца, часы, цепочки, кресты и
                    др.),
                    чтобы
                    не поранить себя и других.</p>
                <p>6. Строго соблюдайте правила личной гигиены: следите за чистотой тела и состоянием ногтей, которые
                    должны
                    быть
                    коротко подстрижены.</p>
                <p>7. На Вас не должно быть никакой косметики.</p>
                <p>8. Уважайте необходимые для занятий вещи и инвентарь.</p>
                <p>9. Доги (кимоно, костюм для занятий) - состоит из куртки,
                    штанов белого цвета и пояса - должно всегда быть чистым и опрятным. </p>
                <p>10. Никогда не прикасайтесь без разрешения к
                    оружию партнёра.</p>
                <p>11. Никогда не одалживайте у кого-либо кимоно или оружие. </p>
                <p>12. Полы куртки запахиваются слева направо (независимо от пола занимающегося), узел пояса
                    завязывается так,
                    чтобы его концы были одинаковой длины. Это последнее действие перед вступлением на татами в
                    додзё.</p>
                <p>13. Ступив на татами, прежде всего произведите рэй в сторону шомэн (лицевую сторону зала), а затем
