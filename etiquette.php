                    сэнсэй (сэмпай) поворачивается и происходит взаимное приветствие со словами Оннагаешимас (буквально:
                    "я в
                    ваших руках", что подтверждает желание заниматься мягко и безопасно).</p>
                <p>15. Опоздания на занятия должны быть исключены, однако, если это всё же случилось, лучше дождаться
